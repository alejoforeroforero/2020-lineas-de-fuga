function ponerIframe(enlace, wix)
{
	contenedorProyectos.innerHTML = '';
	contenedorProyectos.style.display = 'block';
	spanX.style.display = 'block';
	audioNav.pause();
	
	var div = cE('div', contenedorProyectos)
	div.id = 'wix';	
	
	if(wix)
	{
		var div = cE('div', contenedorProyectos);
		div.innerHTML = '';
		div.id = 'wix';	
	}
	
	var iframeContenedor = cE('div', contenedorProyectos);
	iframeContenedor.id = 'iframeContenedor';
	iframeContenedor.style.height = window.innerHeight + 'px';
	
	var iframe = cE('iframe', iframeContenedor);
	iframe.src = enlace;
}

function abrirEnlace(enlace, imagen)
{
	contenedorProyectos.innerHTML = '';
	contenedorProyectos.style.display = 'block';
	spanX.style.display = 'block';
	audioNav.pause();
	
	var div = cE('div', contenedorProyectos);
	div.id = 'imagenEnlace';
	div.style.height = window.innerHeight + 'px';
	
	var img = cE('img', div);
	img.src = imagen;
	img.onclick = function()
	{
		window.open(enlace, '_blank');	
	}
	
	var div = cE('div', div);
	
	var a = cE('a', div);
	a.className = 'enlace';
	a.innerHTML = 'Visitar el proyecto';
	a.href = enlace;
	a.target = '_blank';	
}